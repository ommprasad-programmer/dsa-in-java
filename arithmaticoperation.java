package javadsa;

public class arithmaticoperation {
    public static void main(String[] args) {
        int x=10; int y=3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        double a=10; double b=3;
        System.out.println(a+b);
        System.out.println(a-b);        
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
